package lambda.day3;

public class Computer {

  public static int adder(int x, int y) {
    return (x + y);
  }

  public static int multiply(int x, int y) {
    return (x * y);
  }

  public int adderInstance(int x, int y) {
    return (x + y);
  }

  public int multiplyInstance(int x, int y) {
    return (x * y);
  }

}
